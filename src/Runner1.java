public class Runner1 {
    public static void main(String[] args) {
        //System - класс
        //out - его открытое static поле. (PrintStream класс out)
        //println - метод класса PrintStream
        System.out.println("Hello Java!");//в конце сообщения переходит на новую строку
        System.out.print("Hello Java!");//не переходит на новую строку
        System.out.printf("Hello Java!");//для форматированного вывода(%d %.2f)
    }
}
